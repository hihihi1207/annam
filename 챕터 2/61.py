# 1) 변수명은 영문 및 숫자로 시작해야 한다.
#  변수는 숫자로 시작할 수는 없다.
#   예:  1name (X),  name1 (O),  _name (O)

# 2) 변수명은 언더바(_)로 시작할 수 있다.
#  변수명은 반드시 영문자나 언더바(_)로 시작 가능하다.
#   예:  _value = 10   (O)

# 3) 변수명은 띄어쓰기를 허용한다.
#  변수명에는 공백(띄어쓰기)이 들어갈 수 없다.
#   예:  user name (X),  username (O)

# 4) 변수명으로 예약어를 사용할 수 있다.
#  파이썬에서 정해진 예약어는 변수명으로 사용 불가.
#   예:  if = 10   (X),  while = 5   (X)